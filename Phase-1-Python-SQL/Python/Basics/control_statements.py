num=7
# IF Loop
print("\nThis is IF Loop:")
if num > 0:
    print(f"Number {num} is positive")
else:
    print(f"Number {num} is negative")
print()
# For Loop
print("\nThis is For Loop:")
for i in range(num):
    print (i,end=' ')
print()

# While Loop
print("\nThis is while Loop:")
count = 0
while(count < 5):
    count +=1
    print(count,end=' ')
print()